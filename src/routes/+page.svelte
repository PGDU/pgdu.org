<h1>Welcome to PgDU</h1>
<p>
	PostgreSQL Down Under Incorporated, known as PGDU, is a non for profit association. Our purpose is
	to support the growth and education of PostgreSQL, The World's Most Advanced Open Source Database.
</p>
<p>
	After successful conferences in Melbourne in 2018 and Sydney in 2019 we have now regrouped to hold
	a conference on <b>Friday 17th of October 2025 in Sydney</b>. Stay tuned for venue and details to
	follow.
</p>
<p>
	Please send us an email at <a href="mailto:pgdu-committee@lists.pgdu.org"
		>pgdu-committee@lists.pgdu.org</a
	> if you have any questions.
</p>

